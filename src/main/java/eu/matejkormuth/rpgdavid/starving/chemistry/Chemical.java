package eu.matejkormuth.rpgdavid.starving.chemistry;

import org.bukkit.entity.Player;

public class Chemical {
    private final String name;

    public Chemical(final String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result
                + ((this.name == null) ? 0 : this.name.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Chemical other = (Chemical) obj;
        if (this.name == null) {
            if (other.name != null)
                return false;
        } else if (!this.name.equals(other.name))
            return false;
        return true;
    }

    public Reaction reactionWith(ChemicalCompound chemicalCompound) {
        return Reaction.NONE;
    }

    public void onPureConsumedBy(Player player, float amount) {
    }
}
